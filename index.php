<h3 style="color:blue">How Do I Keep My Pool Crystal Clear?</h3 style="color:blue">
<p>
	The key to keeping your pool crystal clear all summer long is
	<strong>remembering the three C’s of pool care:</strong>
</p>
<ul>
	<li>
		<strong>Circulation –</strong> Water must be circulating via the pump, filtration system, skimmers, and
		 other water features constantly to ensure any debris is filtered out and
		 the water does not go stagnant.
	</li>
	<li>
		<strong>Cleaning –</strong> Regular brushing of the walls, steps, ladders, and other low circulation
		 areas is a must, along with skimming off the top of the surface to collect
		 fallen foliage. Vacuuming is also important to collect any debris that
		 has sunk. Using an automatic cleaner makes this even easier.
	</li>
	<li>
		<strong>Chemistry –</strong> Testing your water weekly is key to keeping the pH and chlorine levels
		 balanced. If things aren’t balanced, you may begin to develop algae
		 or subject yourself to dangerous chlorine levels. Depending on your situation,
		 a chlorine shock may be needed to sanitize and get the water back to normal levels.
	</li>
</ul>
<p>Having our team develop and implement a customized swimming pool maintenance
	 plan can be the best way to ensure your pool is ready and able to be enjoyed
	 all season long!</p>
<h3 style="color:blue">How Often Should I Put Chlorine in The Pool?</h3 style="color:blue">
<p>
	Without knowing the specifics of your pool, it’s quite impossible
	 to give an exact answer of how often or how much chlorine you should be using.
	<strong>The ideal chlorine level for your pool should be between 1 and 1.5 ppm.</strong> If things aren’t at the right level, you may need a chlorine shock
	 or to add a chlorine neutralizing chemical to get things back to normal.
</p>
<p>After this, it’s important to keep your pool at optimal chlorine
	 levels via chlorine tablets. You can determine the exact amount and frequency
	 of chlorine needed by the volume of water, frequency of use, and the number
	 of hours the pool spends in direct sunlight. Our team can take care of
	 all of this for you!</p>
<h3 style="color:blue">Is It Better to Run a Pool Pump at Night or Day?</h3 style="color:blue">
<p>It is always best to run your pool pump during the day, specifically at
	 the hottest time of the day. This is because the sun eats away at your
	 chlorine, so running things during the day gives the chlorine a chance
	 to do its job instead of standing in a still pool being depleted by the
	 sun. If you have people in the pool during the day, running the pump can
	 suck up any foliage brought in and filter out sunscreen residue and other bacteria.</p>
<h3 style="color:blue">What Happens If You Don't Backwash Your Pool?</h3 style="color:blue">
<p>It’s important to backwash your pool about once a week unless otherwise
	 directed by a professional pool technician. A backwash plays an important
	 role in your pool’s cleanliness and sanitization by reversing the
	 flow of the filter and flushing out the water lines. If you ignore this
	 maintenance task, the flow of your system will go down, the pressure will
	 be reduced, and in some cases, the water filter can explode causing major
	 damage to your equipment and possible bodily harm.</p>
